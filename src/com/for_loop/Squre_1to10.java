package com.for_loop;

public class Squre_1to10 {
	
	public static void main(String[] args) {
		
		//19.Print squares of numbers from 1 to 10 using for loop.
		System.out.println("Squre of 1 to 10\n");
		
		for (int i = 1; i <= 10; i++) {
			
			System.out.println(i+" * "+i+" = "+(i*i));
			
		}
	}

}
