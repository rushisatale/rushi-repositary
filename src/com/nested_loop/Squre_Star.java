package com.nested_loop;

public class Squre_Star {
	
	public static void main(String[] args) {
		
		//41.Print square star pattern using nested loops.
		
		for (int i = 1; i <= 4; i++) {
			
			for (int j = 1; j <= 4; j++) {
				
				System.out.print("* ");
			}
			
			System.out.println();
		}
	}

}
